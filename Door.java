public class Door {
    private int x, y;
    
    // 3.a Constructor
    public Door(int x, int y) {
        this.x = x;
        this.y = y;
    }
    //3.b. Accessors

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
    
}
